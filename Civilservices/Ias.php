        <h3>
            IAS is the crème of the civil services. Only the top 100 or less scorers in UPSC become IAS. These individuals hold positions such as District Collector, District 
            Magistrate and can go on to become secretaries in various ministries.
        </h3>
       
        This is one of the most coveted posts in the Civil Services, due to the prestige attached to it. People in these posts are provided with amenities such as a house, servants,
        car, and their living expenses are taken care of by the government.IAS officers are responsible for the law and order, as well as the implementation of government policies
        in day to day life.

<h3>Job Role – </h3>In the initial stages of their career, IAS officers join as the Sub-Divisional Magistrates (SDM) in a state, and go on to occupy positions such as District
Magistrate, Divisional Commissioner, etc. They are appointed as the heads of Government Organizations, Public Service Undertakings and also act as Secretary to the Government 
of India.

<h3>A Day in the Life –</h3> An IAS officer has no “fixed” working hours. The work that they do is hectic, since implementing policies and working to develop the nation is 
challenging, yet, interesting and the impact is huge. Their day can be filled with meetings with ministers, paying visits to a local village or coming up with new strategies 
to solve any issue at hand (such as, work related to elections, or building a big dam, road, etc.)
